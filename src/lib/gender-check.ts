/** Слова в женском роде, найденные в тексте (для подсветки и предупреждения). */
export function detectFeminine(text: string): string[] {
  const words = (text || "").toLowerCase().replace(/[ʼ'`’]/g, "").match(/[а-яёіїєґ]+/gi) || [];
  const found = new Set<string>();
  for (const w of words) if (FEMININE.has(w)) found.add(w);
  return [...found];
}
