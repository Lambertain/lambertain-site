/** Человекочитаемое представление ответа (для пузырька в чате и сводки). */
export function answerLabel(f: Field, data: Record<string, unknown>, lang: Lang): string {
  const v = data[f.key];
  const other = String(data[f.key + "Other"] ?? "").trim();
  if (f.kind === "multi") {
    const picked = (Array.isArray(v) ? (v as string[]) : []).map((k) => f.opts?.find((o) => o.key === k)?.[lang] || k);
    if (other) picked.push(other);
    return picked.join(", ") || TXT.skip[lang];
  }
  if (f.kind === "yesno") {
    const base = v === "yes" ? TXT.yes[lang] : v === "no" ? TXT.no[lang] : TXT.skip[lang];
    return other ? `${base} (${other})` : base;
  }
  const s = String(v ?? "").trim();
  return s || TXT.skip[lang];
}
